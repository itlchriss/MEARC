package g1301_1400.s1313_decompress_run_length_encoded_list;

// #Easy #Array #2022_06_20_Time_1_ms_(98.11%)_Space_42.5_MB_(98.11%)

public class Solution {
	//@ requires(*The input array `nums` is not null.*);
	//@ requires(*The length of `nums` is at least 2.*);
	//@ requires(*The length of `nums` is even.*);
	//@ requires(*The values in `nums` are positive integers.*);
	//@ requires(*The values in `nums` are between 1 and 100 (inclusive).*);
	//@ ensures(*The returned array is not null.*);
	//@ ensures(*The length of the returned array is equal to the sum of all the even-indexed elements in `nums`.*);
	//@ ensures(*The values in the returned array are the concatenation of sublists generated from adjacent pairs of elements in `nums`.*);
	//@ ensures(*The sublists are generated by repeating the value of the odd-indexed element in the pair, the number of times specified by the even-indexed element in the pair.*);
	//@ ensures(*The sublists are concatenated from left to right.*);
    public int[] decompressRLElist(int[] nums) {
        int len = 0;
        for (int i = 0; i < nums.length; i += 2) {
            len += nums[i];
        }
        int[] ans = new int[len];
        int index = 0;
        for (int i = 0; i < nums.length; i += 2) {
            int j = nums[i];
            while (j > 0) {
                ans[index] = nums[i + 1];
                index++;
                j--;
            }
        }
        return ans;
    }
}